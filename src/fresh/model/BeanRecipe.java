package fresh.model;

import java.io.File;

public class BeanRecipe {
	private int Recipe_num;
	private String Recipe_name;
	private String Recipe_mater;
	private String Recipe_step;
	private String Recipe_picture;
	public int getRecipe_num() {
		return Recipe_num;
	}
	public void setRecipe_num(int recipe_num) {
		Recipe_num = recipe_num;
	}
	public String getRecipe_name() {
		return Recipe_name;
	}
	public void setRecipe_name(String recipe_name) {
		Recipe_name = recipe_name;
	}
	public String getRecipe_mater() {
		return Recipe_mater;
	}
	public void setRecipe_mater(String recipe_mater) {
		Recipe_mater = recipe_mater;
	}
	public String getRecipe_step() {
		return Recipe_step;
	}
	public void setRecipe_step(String recipe_step) {
		Recipe_step = recipe_step;
	}
	public String getRecipe_picture() {
		return Recipe_picture;
	}
	public void setRecipe_picture(String recipe_picture) {
		Recipe_picture = recipe_picture;
	}
	
}
