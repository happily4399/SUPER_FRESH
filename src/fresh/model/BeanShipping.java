package fresh.model;

public class BeanShipping {
	private int ship_num;
	private int User_num;
	private String ship_pro;
	private String ship_city;
	private String ship_area;
	private String ship_address;
	private String ship_con;
	private String ship_tele;
	public int getShip_num() {
		return ship_num;
	}
	public void setShip_num(int ship_num) {
		this.ship_num = ship_num;
	}
	public int getUser_num() {
		return User_num;
	}
	public void setUser_num(int user_num) {
		User_num = user_num;
	}
	public String getShip_pro() {
		return ship_pro;
	}
	public void setShip_pro(String ship_pro) {
		this.ship_pro = ship_pro;
	}
	public String getShip_city() {
		return ship_city;
	}
	public void setShip_city(String ship_city) {
		this.ship_city = ship_city;
	}
	public String getShip_area() {
		return ship_area;
	}
	public void setShip_area(String ship_area) {
		this.ship_area = ship_area;
	}
	public String getShip_address() {
		return ship_address;
	}
	public void setShip_address(String ship_address) {
		this.ship_address = ship_address;
	}
	public String getShip_con() {
		return ship_con;
	}
	public void setShip_con(String ship_con) {
		this.ship_con = ship_con;
	}
	public String getShip_tele() {
		return ship_tele;
	}
	public void setShip_tele(String ship_tele) {
		this.ship_tele = ship_tele;
	}
	
}
